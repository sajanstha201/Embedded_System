	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
    DCD 0X40001000       ; STACK POINTER VALUE WHEN STACK IS EMPTY
    DCD RESET_HANDLER    ; RESET VECTOR
    ALIGN

	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT RESET_HANDLER

RESET_HANDLER
	LDR R0, =NUM
 	LDR R1, [R0]
  	LDR R7, =0X0000FFFF
AND R2, R1, R7
LDR R9,=0X00FF
  	AND R3, R2, R9
	LDR R9,=0XFF00
    	AND R4, R2, R9
    	MOV R4, R4, LSR #4
    	ORR R6, R4, R3

    	LDR R7, =0XFFFF0000
    	AND R2, R1, R7
    	LDR R7, =0X00FF0000
    	AND R3, R2, R7
    	LDR R7, =0XFF000000
    	AND R4, R2, R7
    	MOV R4, R4, LSR #4
    	ORR R5, R4, R3

    	MOV R5, R5, LSR #8
    	ORR R5, R5, R6
    	LDR R8, =RES
    	STR R5, [R8]

STOP
 	B STOP
NUM DCD 0X02030506
	AREA DDD, DATA, READWRITE
RES DCD 0
	END
