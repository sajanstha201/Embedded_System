AREA MYCODE, CODE, READONLY
	EXPORT __VECTORS

__VECTORS
	DCD 0X10001000
	DCD RESET_HANDLER
	ALIGN 4

	AREA CODEE, CODE, READONLY
	ENTRY
	EXPORT RESET_HANDLER

RESET_HANDLER
	LDR R0, =SRC          
	LDR R1, =DST          
	MOV R2, #10           
LOOP
	LDR R3, [R0], #4      
	STR R3, [R1], #4      
	SUBS R2,#1       
	BNE LOOP              

STOP
	B STOP                

	AREA CD, DATA, READONLY
SRC DCD 8, 3, 2, 1, 4, 6, 7, 9, 4, 1  
	AREA DD, DATA, READWRITE
DST DCD 0                              
	END
