AREA DD,DATA,READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0X10001000
	DCD RESET_HANDLER
	ALIGN
	AREA MYCODE,CODE,READONLY
	ENTRY
	EXPORT RESET_HANDLER
RESET_HANDLER
	LDR R0,=NUM1
	LDR R1,=NUM2
	LDR R2,=DST
	LDR R3,[R0]
	LDR R4,[R1]
	MOV R5,#0
LOOP
	ADD R5,R5,R4
	SUBS R3,#1
	BNE LOOP
	STR R5,[R2]
NUM1 DCD 0X656
NUM2 DCD 0X26
	AREA DDD,DATA,READWRITE
DST DCD 0
	END
