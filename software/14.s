	AREA RESET, DATA, READONLY
	EXPORT __VECTORS

__VECTORS
	
	DCD 0X1001000
	DCD RESET_HANDLER
	ALIGN
	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT RESET_HANDLER
	
RESET_HANDLER	
	
	LDR R0,=SRC
	LDR R1,=DST
	MOV R2,#0
	LDR R3,[R0]
	MLA R2,R3,R3,R3         ; R3 * R3 + R3
	LSR R2,#1
	STR R2,[R1]
STOP
	B STOP
SRC DCD 5
	AREA MYDATA, DATA, READWRITE
DST DCD 0
	END
